plot(Slg,Runs)
RegObp<-lm(Runs~Obp,data=tstats_npb)
RegAvg<-lm(Runs~Avg,data=tstats_npb)
RegSlg<-lm(Runs~Slg,data=tstats_npb)
summary(RegAvg)
summary(RegSlg)
summary(RegObp)
cor(Avg,Runs)
cor(Obp,Runs)
cor(Slg,Runs)
cor(Avg,WA)
cor(Obp,WA)
cor(Slg,WA)
OPS=Obp+Slg
RegOPS<-lm(Runs~OPS,data=tstats_npb)
