library(data.table)
tstats_npb<-fread("C:/Users/T-Reio/Master_thesis/team_stats_npb.csv",header=T,sep=",")
summary(tstats_npb)
Avg<-tstats_npb$AVG
Obp<-tstats_npb$OBP
WA<-tstats_npb$WA
Slg<-tstats_npb$SLG
Runs<-tstats_npb$Runs
plot(Avg,Runs)
